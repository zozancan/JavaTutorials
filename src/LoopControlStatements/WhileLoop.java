package LoopControlStatements;

public class WhileLoop {

    public static void main(String[] args) {

        int y = 0;
        while (y < 5) {
            System.out.println(y);
            y++;
        }

        int z = 0;
        do {
            System.out.println(z);
            z++;
        }
        while (z < 5);



    }
}
